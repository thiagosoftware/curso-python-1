# Solicita ao usuário que digite o seu nome e armazena a resposta na variável 'nome'
nome = input("Digite seu nome: ")

# O comando 'print' é usado para exibir uma mensagem no console. 
# A letra 'f' antes das aspas indica que estamos usando uma string formatada (f-string), 
# o que permite inserir variáveis dentro da string de forma simples e direta.
# A variável 'nome' será inserida no local indicado por {nome} dentro da string.
print(f"Olá {nome}, seja bem-vindo(a) ao curso de Python 1 - fundamentos no Senac Osasco!")

# A função 'input()' solicita que o usuário digite algo no terminal
# O texto digitado será armazenado em uma variável chamada 'nome'
nome = input("Digite seu nome: \n")

# Aqui, a função 'print()' é utilizada para exibir o conteúdo da variável 'nome' na tela
print(nome)

# Uma variável em Python pode ser considerada um "recipiente" que armazena dados temporariamente
# Neste caso, a variável 'nome' armazenou uma string, que é uma cadeia de caracteres.

# 2 - Desenvolva um programa em Python que calcule a área de um triângulo usando a fórmula: (base * altura) / 2.

base = float(input('Digite a base do triângulo: \n'))
altura = float(input('Digite a altura do triângulo: \n'))

area = (base * altura) / 2

print(f'Considerando que a base meça {base} e a altura {altura}, a área desse triângulo é {area}')